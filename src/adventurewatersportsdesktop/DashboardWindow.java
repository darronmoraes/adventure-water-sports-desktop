    // Current date report on dashboard
    private void fetchCurrentDateReport() {
        /**
         * Use Date class to create an object and use date formatter for
         * formatting the date.
         * 
         */
        
        Date currentDate = new Date();
        
        // Create a date format to format the date as a String.
        SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
        String currentDateStringFormat = dateFormat.format(currentDate);
        
        // Print the cuurent date
        System.out.println("Current Date: " + currentDateStringFormat);
        
        try {
            // Create the URL with query parameter
            // query param variable
            String dateQueryParam = "?date=";

            String apiUrl = Constants.URL + Constants.CURRENT_DATE_REPORT + dateQueryParam + currentDateStringFormat;
            URL url = new URL(apiUrl);

            // Open a connection to the URL
            HttpURLConnection connection = (HttpURLConnection) url.openConnection();

            // Set the HTTP request method to GET
            connection.setRequestMethod("GET");

            // Get the HTTP response code
            int responseCode = connection.getResponseCode();

            if (responseCode == HttpURLConnection.HTTP_OK) {
                try (BufferedReader br = new BufferedReader(new InputStreamReader(connection.getInputStream()))) {
                    StringBuilder response = new StringBuilder();
                    String line;
                    while ((line = br.readLine()) != null) {
                        response.append(line);
                    }

                    // Parse the JSON response
                    JSONArray jsonArray = new JSONArray(response.toString());

                    if (jsonArray.length() > 0) {
                        // 
                        JSONObject item = jsonArray.getJSONObject(0);

                        // Create an instance of report model class and populate
                        Report currentDateReport = new Report(
                                item.getDouble("commission"),
                                item.getString("order_date"),
                                item.getString("pax"),
                                item.getDouble("total_amount")
                        );

                        // Update the swing labels with extracter data
                        jLabelCommissionAmount.setText(String.valueOf(currentDateReport.getCommission()));
                        jLabelPaxCount.setText(currentDateReport.getPax());
                        jLabelTotalAmount.setText(String.valueOf(currentDateReport.getTotal_amount()));
                    } else {
                        jLabelCommissionAmount.setText("0");
                        jLabelPaxCount.setText("0");
                        jLabelTotalAmount.setText("0");
                    }
                } catch (Exception e) {
                    e.printStackTrace();
                }
            }
        } catch (IOException e) {
            e.getMessage();
        }
    }
    
    // function to make api call to fetch report data based on from and to dates
    protected void fetchReportBasedOnFromAndToDates(String from, String to) {
        
        // Create defaultTableModel object and use tableReport instance.
        DefaultTableModel tableModel = (DefaultTableModel) tableReport.getModel();
        
        try {
            String fromDateQueryParam = "?firstdate=";
            String toDateQueryParam = "&lastdate=";
            
            String apiUrl = Constants.URL + Constants.RANGE_DATE_REPORT + fromDateQueryParam + from + toDateQueryParam + to;
            //String apiUrl = "http://127.0.0.1:5000/get_report_between_dates?firstdate=2023-07-26&lastdate=2023-07-30";
            
            // Fetch data from API
            URL url = new URL(apiUrl);
            HttpURLConnection connection = (HttpURLConnection) url.openConnection();
            connection.setRequestMethod("GET");
            
            // Get the response code
            int responseCode = connection.getResponseCode();
            
            if (responseCode == HttpURLConnection.HTTP_OK) {
                // Read the respons from the connection's input
                try (BufferedReader br = new BufferedReader(new InputStreamReader(connection.getInputStream()))) {
                    StringBuilder response = new StringBuilder();
                    String line;
                    while ((line = br.readLine()) != null) {
                        response.append(line);
                    }
                    
                    // Parse the JSON response
                    JSONArray jsonArray = new JSONArray(response.toString());
                    
                    // Clear the existing data in the table if any.
                    tableModel.setRowCount(0);
                    
                    // Add data to the table
                    for (int i = 0; i < jsonArray.length(); i++) {
                        JSONObject item = jsonArray.getJSONObject(i);
                        
                        // Add data to the tables model
                        tableModel.addRow(new Object[]{i+1, item.getString("order_date"), item.getString("pax"), item.getBigDecimal("total_amount"), item.getBigDecimal("commission")});
                        
                    }  // End of For loop
                }   // End of Try block
            }   // End of If block
        } catch (IOException | JSONException e) {
            e.printStackTrace();
        }
        
        
        
    }

